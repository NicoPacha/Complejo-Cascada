    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if (txtAgregar.getText().equals("") || txtAgregar.getText().equals("0") || txtAgregar.getText().equals("00") || txtAgregar.getText().equals("000")) {
            JOptionPane.showMessageDialog(null, "Indique el numero de departamento a agregar", "ERROR!", JOptionPane.CANCEL_OPTION);
        } else {
            int num = Integer.parseInt(txtAgregar.getText());
            if (gdb.comprobarDepartamento(num) == false) {
                gdb.agregarDepartamento(num);
                txtAgregar.setText("");
                DesktopNotify.showDesktopMessage("Registrado con exito","Departamento "+num+" añadido", DesktopNotify.SUCCESS, 7000);
            } else {
                JOptionPane.showMessageDialog(null, "Ya existe", "Error", JOptionPane.CANCEL_OPTION);
            }
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (txtEliminar.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Indique el numero de departamento a eliminar", "ERROR!", JOptionPane.CANCEL_OPTION);
        } else {
            int num = Integer.parseInt(txtEliminar.getText());
            gdb.eliminarDepartamento(num);
            txtEliminar.setText("");
            DesktopNotify.showDesktopMessage("Eliminado con exito","Departamento "+num+" eliminado", DesktopNotify.INFORMATION, 7000);
        }


    }//GEN-LAST:event_btnEliminarActionPerformed

